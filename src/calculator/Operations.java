package calculator;

public class Operations {

    public float Addition(float n1, float n2){
        float result;
        result = n1 + n2;

        return result;
    }

    public float Subtraction(float n1, float n2){
        float result;
        result = n1 - n2;

        return result;
    }

    public float Multiplication(float n1, float n2){
        float result;
        result = n1 * n2;

        return result;
    }

    public float Division(float n1, float n2){
        float result;
        result = n1 + n2;

        return result;
    }
}
